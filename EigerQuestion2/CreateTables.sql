CREATE TABLE DEPARTMENT (
    ID int NOT NULL AUTO_INCREMENT,
    NAME varchar(255),
    LOCATION  varchar(255),
    PRIMARY KEY (ID)
);
CREATE TABLE EMPLOYEE (
    ID int NOT NULL AUTO_INCREMENT,
    NAME varchar(255),
    SALARY  int,
    DEPT_ID int,
    PRIMARY KEY (ID),
    FOREIGN KEY (DEPT_ID) REFERENCES DEPARTMENT(ID)
);